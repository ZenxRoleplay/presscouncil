    <!-- ============================================================
         2. MANUSCRIPT PREPARATION
    ============================================================ -->
    <div class="policy-section" id="preparation">
      <div class="policy-section-header">
        <span>&#9654;</span> Manuscript Preparation
      </div>
      <div class="policy-section-body">
        <strong>Formatting Requirements</strong>
        <ul>
          <li><strong>Language:</strong> English or Marathi</li>
          <li><strong>Word Limit:</strong> 4,000–8,000 words (including references; excluding abstract and title page)</li>
          <li><strong>File Format:</strong> Microsoft Word (.docx) only</li>
          <li><strong>Font:</strong> Times New Roman, 12pt</li>
          <li><strong>Line Spacing:</strong> 1.5</li>
          <li><strong>Margins:</strong> 1 inch on all sides</li>
          <li><strong>Referencing Style:</strong> APA 7th Edition</li>
          <li><strong>Page Numbers:</strong> Bottom centre, beginning from page 1</li>
        </ul>

        <strong>Required Sections (in order)</strong>
        <ul>
          <li><strong>Title:</strong> Concise and descriptive (max 20 words)</li>
          <li><strong>Author Details:</strong> Full name, designation, department, institution, city, country, and institutional email for each author. Corresponding author must be indicated.</li>
          <li><strong>Abstract:</strong> 150–250 words; structured as: Background, Objective, Methodology, Findings, Conclusion</li>
          <li><strong>Keywords:</strong> 4–6 keywords, separated by semicolons</li>
          <li><strong>Introduction</strong></li>
          <li><strong>Literature Review / Theoretical Framework</strong></li>
          <li><strong>Methodology</strong></li>
          <li><strong>Results &amp; Discussion</strong></li>
          <li><strong>Conclusion</strong></li>
          <li><strong>References</strong> (APA 7th Edition)</li>
          <li><strong>Acknowledgements</strong> (if applicable)</li>
          <li><strong>Conflict of Interest Statement</strong></li>
        </ul>

        <strong>Figures, Tables &amp; Images</strong>
        <ul>
          <li>All figures and tables must be numbered sequentially (Table 1, Figure 1, etc.)</li>
          <li>Each must have a descriptive caption placed directly above (tables) or below (figures)</li>
          <li>Images must be embedded in the document AND submitted separately as high-resolution .jpg or .png (minimum 300 DPI)</li>
          <li>Data sources must be cited for all tables and figures</li>
        </ul>
      </div>
    </div>

