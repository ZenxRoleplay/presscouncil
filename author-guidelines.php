    <!-- ============================================================
         5. AUTHORSHIP & ORIGINALITY
    ============================================================ -->
    <div class="policy-section" id="authorship">
      <div class="policy-section-header">
        <span>&#9654;</span> Authorship &amp; Originality
      </div>
      <div class="policy-section-body">
        <ul>
          <li>All listed authors must have made a substantial intellectual contribution to the work.</li>
          <li>Manuscripts must be original, unpublished, and not simultaneously submitted elsewhere.</li>
          <li>The similarity index must not exceed <strong>15%</strong> overall (verified by the editorial office using anti-plagiarism detection software).</li>
          <li>Manuscripts found to involve plagiarism, data fabrication, or duplicate submission will be rejected and the authors' affiliated institutions will be notified.</li>
          <li>Any changes to authorship after submission must be approved in writing by all listed authors.</li>
        </ul>
      </div>
    </div>

