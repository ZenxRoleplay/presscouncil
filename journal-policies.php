    <!-- ============================================================
         3. ETHICS STATEMENT
    ============================================================ -->
    <div class="policy-section" id="ethics">
      <div class="policy-section-header">
        <span>&#9654;</span> Publication Ethics Statement
      </div>
      <div class="policy-section-body">
        <p>
          The <em>Press Council of Maharashtra Journal</em> is committed to the highest standards of
          publication ethics and to the prevention of malpractice. All parties involved in the act of
          publishing — authors, editors, editorial board members, and reviewers — are required to adhere
          to the principles set out in the Guidelines of the
          <strong>Committee on Publication Ethics (COPE)</strong>.
        </p>

        <strong>Duties of Authors</strong>
        <ul>
          <li>Authors must ensure that the work submitted is original and has not been published elsewhere in any language, in whole or in part.</li>
          <li>Authors must accurately describe the methods and data used to support the conclusions.</li>
          <li>Authorship should be limited to those who have made a significant contribution to the conception, design, or execution of the work.</li>
          <li>Authors must identify all sources used in the preparation of the manuscript and cite them appropriately.</li>
          <li>Authors must promptly notify the editor if they detect an error or inaccuracy in a published or submitted work.</li>
        </ul>

        <strong>Duties of Editors</strong>
        <ul>
          <li>Editors shall evaluate manuscripts solely on their intellectual merit, without regard to the author's race, gender, religious belief, nationality, or political philosophy.</li>
          <li>Editors shall not disclose any information about a submitted manuscript to any person other than the corresponding author and designated reviewers.</li>
          <li>Editors shall take reasonable, proportionate action when ethical complaints are received concerning a submitted or published article.</li>
        </ul>

        <strong>Duties of Reviewers</strong>
        <ul>
          <li>Reviewers must maintain strict confidentiality regarding all details of the manuscripts reviewed.</li>
          <li>Reviewers shall not use information from a manuscript under review for their own advantage.</li>
          <li>Reviewers must declare any conflict of interest and disqualify themselves from the review process if such a conflict exists.</li>
          <li>Reviews must be conducted objectively and supported by evidence and reasoned argument.</li>
        </ul>
      </div>
    </div>

    <!-- ============================================================
         4. PEER REVIEW POLICY
    ============================================================ -->
    <div class="policy-section" id="peer-review">
      <div class="policy-section-header">
        <span>&#9733;</span> Peer Review Policy
      </div>
      <div class="policy-section-body">
        <p>
          All original research articles, review papers, and case studies submitted to the
          <em>Press Council of Maharashtra Journal</em> are subject to a <strong>double-blind peer review
          process</strong> in which neither the author(s) nor the reviewers are aware of each other's
          identity.
        </p>

        <strong>Review Process</strong>
        <ul>
          <li><strong>Initial Screening:</strong> All submissions are first assessed by the Editor-in-Chief
              for scope, originality, and compliance with submission guidelines. Manuscripts failing to meet
              minimum standards are desk-rejected without external review.</li>
          <li><strong>External Review:</strong> Manuscripts passing initial screening are assigned to a
              minimum of two independent experts with relevant subject expertise.</li>
          <li><strong>Decision:</strong> Possible decisions include: Accept as is; Minor Revisions Required;
              Major Revisions Required; Reject. Final publication decisions rest solely with the
              Editor-in-Chief.</li>
          <li><strong>Turnaround:</strong> The editorial office aims to communicate a first decision within
              <strong>6–8 weeks</strong> of submission.
        </ul>

        <p>
          The journal does not accept unsolicited reviews. All reviewers are invited by the Editorial Board
          on the basis of their expertise, and are expected to declare any conflicts of interest.
        </p>
      </div>
    </div>

    <!-- ============================================================
         5. PLAGIARISM POLICY
    ============================================================ -->
    <div class="policy-section" id="plagiarism">
      <div class="policy-section-header">
        <span>&#9998;</span> Plagiarism &amp; Originality Policy
      </div>
      <div class="policy-section-body">
        <p>
          The <em>Press Council of Maharashtra Journal</em> takes plagiarism, self-plagiarism, and
          academic misconduct very seriously. All manuscripts are screened for similarity using an
          anti-plagiarism detection tool prior to peer review.
        </p>

        <strong>Acceptable Similarity Thresholds</strong>
        <ul>
          <li>Overall similarity index: <strong>Not exceeding 15%</strong></li>
          <li>Similarity from a single source: <strong>Not exceeding 5%</strong></li>
          <li>Quotations properly attributed and referenced are excluded from the similarity calculation.</li>
        </ul>

        <strong>Consequences of Misconduct</strong>
        <ul>
          <li>Manuscripts found to contain plagiarism at the screening stage will be rejected immediately
              without review.</li>
          <li>If plagiarism is discovered post-publication, the article will be retracted and a notice of
              retraction will be published on the journal website.</li>
          <li>Authors found guilty of repeated academic misconduct may be barred from submitting to this
              journal for a period of <strong>3 years</strong>.</li>
          <li>Cases of serious misconduct will be reported to the author's affiliated institution.</li>
        </ul>

        <p>
          Authors are required to declare, at the time of submission, that the manuscript is original,
          has not been previously published, and is not under consideration for publication elsewhere.
        </p>
      </div>
    </div>

